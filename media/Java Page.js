var found = false;
function search(search) {
    found = false;
    modal.style.display = 'block';
    database.forEach(function (item, i) {
        if (item.word.toLowerCase().includes(search) && item && search) {
            found = true;
            //found
            console.log(item)
            document.getElementById("word").innerHTML = item.word
            document.getElementById("def").innerHTML = item.definition
        } else {
            if (found !== true) {
                found = false;           
            }
        }
        if (i == database.length - 1) {
            if (found == false) {
                //not found
                console.log(item)
                document.getElementById("word").innerHTML = "Not Found"
                document.getElementById("def").innerHTML = "Try searching another word, browse our Catalogue or correcting your spelling"
            }
        }
    })
}

function ShowWord(id) {
    modal.style.display = 'block';
    document.getElementById("word").innerHTML = database[id].word
    document.getElementById("def").innerHTML = database[id].definition
}





const modal = document.querySelector('#my-modal');
const modalBtn = document.querySelector('#modal-btn');
const closeBtn = document.querySelector('.close');


modalBtn.addEventListener('click', search);
closeBtn.addEventListener('click', closeModal);
window.addEventListener('click', outsideClick);


function closeModal() {
  modal.style.display = 'none';
}


function outsideClick(e) {
  if (e.target == modal) {
    modal.style.display = 'none';
  }
}
